/*
Enunciado:
Crie um programa que peça um número e mostre uma mensagem 
dizendo se ele é positivo, negativo ou igual a zero.
*/

let numero = Number (prompt ("Digite seu número"))

if (numero > 0) {
    alert("Positivo")
}else if (numero < 0) {
    alert("Negativo");
}else{
    alert ("Igual")
}



